#include<iostream>
using namespace std;
int main ()
{
    int a=30,b=10;

    //Arithmetic Operators
    cout<<"Arithmetic Operators"<<endl;
    
    cout<<"The value of a+b is :- "<<a+b<<endl;
    cout<<"The value of a-b is :- "<<a-b<<endl;
    cout<<"The value of a*b is :- "<<a*b<<endl;
    cout<<"The value of a/b is :- "<<a/b<<endl;
    cout<<"The value of a%b is :- "<<a%b<<endl;
    cout<<"The value of ++a is :- "<<++a<<endl;
    cout<<"The value of --a is :- "<<--a<<endl;

    cout<<endl;
    cout<<endl;
}